package com.helpPet.app.board.boardAdoptReview.vo;

public class ReviewFilesVO {
	private int reviewBoardNum;
	private String reviewFileName;
	private String reviewFileOriginal;
	
	public ReviewFilesVO() {;}

	public int getReviewBoardNum() {
		return reviewBoardNum;
	}

	public void setReviewBoardNum(int reviewBoardNum) {
		this.reviewBoardNum = reviewBoardNum;
	}

	public String getReviewFileName() {
		return reviewFileName;
	}

	public void setReviewFileName(String reviewFileName) {
		this.reviewFileName = reviewFileName;
	}

	public String getReviewFileOriginal() {
		return reviewFileOriginal;
	}

	public void setReviewFileOriginal(String reviewFileOriginal) {
		this.reviewFileOriginal = reviewFileOriginal;
	}

	
	
}
